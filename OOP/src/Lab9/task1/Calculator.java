package Lab9.task1;

public class Calculator {

    public int add(int a, int b) {
        System.out.println("Додаємо два цілі числа");
        return a + b;
    }

    public double add(double a, double b) {
        System.out.println("Додаємо два числа з плаваючою крапкою");
        return a + b;
    }

    public int add(int a, int b, int c) {
        System.out.println("Додаємо три цілі числа");
        return a + b + c;
    }
}


